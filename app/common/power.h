#ifndef POWER_H
#define POWER_H

#include <stdint.h>

#include "common/define.h"

namespace olive {

uint32_t ceil_to_power_of_2(uint32_t v)
{
  v--;
  v |= v >> 1;
  v |= v >> 2;
  v |= v >> 4;
  v |= v >> 8;
  v |= v >> 16;
  v++;

  return v;
}

uint32_t floor_to_power_of_2(uint32_t x)
{
  x = x | (x >> 1);
  x = x | (x >> 2);
  x = x | (x >> 4);
  x = x | (x >> 8);
  x = x | (x >> 16);

  return x - (x >> 1);
}

}

#endif // POWER_H
